    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        try {
            Filmes novoFilme = new Filmes();
            novoFilme.setNome(txtNomeFilme.getText());
            novoFilme.setData(txtData.getText());
            novoFilme.setCategoria(txtCategoria.getText());

            FilmesDao filmesDao = new FilmesDao();
            filmesDao.salvar(novoFilme);

            JOptionPane.showMessageDialog(this, "Filme cadastrado com sucesso!");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocorreu uma falha:\n" + e.getMessage());
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

